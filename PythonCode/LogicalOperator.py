# In or operator. If any of them is true then the output will show you "true"
print(2>3 or 2>1)

#In "and" operator if any of them will not relevant then the output will be false
# Here every statement should be right
print(3>2 and 3>4)

# "not" in not operator output will show the opposite.
print(not 3>2)
print(not 2>3)